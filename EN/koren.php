	<h2>Trajecti Voces</h2>
	<p><img src="../images/Trajecti%20ZwW.gif" alt="Trajecti logo" width="300" class="w3-left" style="margin: 0 20px 10px 0; float: left;" /> <a href="www.trajectivoces.net" target="_blank">Trajecti Voces</a> (Utrecht, 1989) is an active, semi-professional vocal ensemble, which performs both early and contemporary music. The core of the ensemble consists of  16 trained singers. They guarantee a consistent high vocal and musical quality  and the distinctive Trajecti sound.  Members and conductor strive to put annually three unusual projects together. The ensemble created in 1998 a theatrical performance (sung from memory!) of Bach's St John Passion, which Dirkjan Horringa conducted AND directed. In 2005 an unorthodox performance of Bach's St Matthew Passion was given in the Utrecht pop-palace 'Tivoli'. In 2008, the choir  performed a theatrical show around the theme of alchemy at the time of the Habsburg Emperor Rudolf II. In 2015 a performance was created around Mozart's membership of the Viennese Freemasons. A hypothetical lodge meeting to commemorate brother Mozart while performing his Masonic music was staged. The performance included his Ave Verum, his Masonic mourning music, parts of the Magic Flute and culminated in his Requiem.</p>
	<p>Trajecti Voces rehearses on Tuesday evenings, mostly at the Pieterskerk, Pieterskerkhof 5, Utrecht.</p>
