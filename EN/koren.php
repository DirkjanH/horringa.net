	<h2>Festina Lente </h2>
	<p>The project choir <a href="https://www.projectkoorfestinalente.nl/index.php" target="_blank">Festina Lente</a>, located in Arnhem, strives for great musical quality, choral sound and expression. The choir focuses on participants from the wider area of Arnhem. Dirkjan Horringa is working with very advanced amateur singers and professional musicians. Solo's are often performed by choir members.</p>
	<p>Project choir Festina Lente wants to specialize in works from Renaissance and Baroque and also makes trips to more contemporary music. Festina Lente will perform both spiritual and worldly music. The choir is always looking for special works and rarely performed music.</p>
	<p>Festina Lente is a good level choir. The singers can sing independently and also color well with each other. That is why Festina Lente wants to work with a loyal solid core supported by a larger group of project singers. The permanent core is a group of singers who structurally connect with the Festina Lente projects. In addition, people are recruited from a pool of advanced singers per project.</p>
	<p>Dirkjan Horringa is our conductor at Festina Lente. During the rehearsals we work hard on the quality of the music, the group sound and the use of the voice, in a cheerful and relaxed atmosphere. Festina Lente organizes projects 2 or 3 times a year.</p>
