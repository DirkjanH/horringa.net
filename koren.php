	<h2>Trajecti Voces</h2>
	<p><img src="images/Trajecti%20ZwW.gif" alt="Trajecti logo" width="300" class="w3-left" style="margin: 0 20px 10px 0; float: left;" />Het Utrechtse vocaal ensemble <a title="Trajecti Voces" href="https://www.trajectivoces.net" target="_blank">Trajecti Voces</a> (1989) is een actief, semi-professioneel ensemble, dat zowel oude als nieuwe muziek uitvoert. De vaste kern van het ensemble bestaat uit 12 geschoolde zangers. Zij staan garant voor een constante koorkwaliteit en de kenmerkende Trajecti-klank. Jaarlijks zetten leden en dirigent zich in om ongewone projecten in elkaar te zetten. Het ensemble voerde in 1998 een theatervoorstelling (uit het hoofd gezongen!) van Bachs Johannes Passion, die Dirkjan Horringa dirigeerde EN regisseerde. In 2005 werd een onorthodoxe uitvoering gegeven van Bachs Matthäus Passion  in de Utrechtse pop-paleis 'Tivoli'. In 2008 voerde het koor een theatrale voorstelling rond het thema van de alchemie aan het hof van de Habsburgse keizer Rudolf II. In 2015 werd een voorstelling gemaakt rond Mozarts lidmaatschap van de Weense vrijmetselaars, in de vorm van een hypothetische orde-bijeenkomst om broeder Mozart te herdenken. Hierin werd veel van zijn maçonnieke muziek uitgevoerd, zoals het Ave Verum, zijn Maurerische Trauermusik, delen van de Zauberflöte. De voorstelling culmineerde in zijn Requiem.</p>
	<p>Trajecti Voces repeteert op de dinsdagavond  in de Rehobothkerk, Briljantlaan 6, Utrecht.</p>
