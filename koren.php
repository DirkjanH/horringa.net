	<h2>Festina Lente</h2>
	<p><strong><img class="content-left" style="margin: 0 20px 10px 0; float: left;" src="images/FestinaLente.jpg" alt="2009 Bremen 077" width="300" /></strong></p>
    <a href="http://projectkoorfestinalente.nl/">Projectkoor Festina Lente</a>, gevestigd in Arnhem, streeft naar grote muzikale kwaliteit, koorklank en expressie. Het koor richt zich op deelnemers uit de wijde omgeving van Arnhem. Er wordt onder leiding van Dirkjan Horringa gewerkt met zeer gevorderde amateurzangers en beroepsmusici. Solodelen worden vaak door koorleden uitgevoerd.<br>
<br>
Projectkoor Festina Lente specialiseert zich in werken uit Renaissance en Barok en maakt ook uitstapjes naar meer hedendaagse muziek. Festina Lente voert zowel geestelijke als wereldlijke muziek uit. Het koor is steeds op zoek naar bijzondere werken en zelden uitgevoerde muziek.<br>
<br>
Festina Lente is een koor van goed niveau. De zangers kunnen zelfstandig zingen en ook goed met elkaar kleuren. Daarom wil Festina Lente met een trouwe vaste kern werken ondersteund door een grotere groep project zangers. De vaste kern is een groep zangers die zich structureel met de projecten van Festina Lente verbindt. Daarnaast worden per project mensen geworven uit een pool van gevorderde zangers.</article>
